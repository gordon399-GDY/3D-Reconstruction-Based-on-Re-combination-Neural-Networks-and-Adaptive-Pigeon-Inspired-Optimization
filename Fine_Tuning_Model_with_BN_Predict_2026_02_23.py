# ---------- 主评估程序 ----------
def main():
    # 模型文件路径（请确保该文件存在于当前目录）
    model_path = 'Deep_Learning_based_3D_Reconstruction_Model.pth'

    if not os.path.exists(model_path):
        print(f"错误：模型文件 {model_path} 不存在！")
        return

    # 设备配置
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"使用设备: {device}")

    # 加载模型
    model = SimplifiedMLP_B(input_dim=4, output_dim=3)
    model.load_state_dict(torch.load(model_path, map_location=device))
    model = model.to(device)
    model.eval()   # 设置为评估模式
    print("模型加载成功！")

    # 准备数据（标准化）
    inputs_scaled, outputs_scaled, input_scaler, output_scaler = prepare_data(raw_data)

    # 转换为 tensor
    inputs_tensor = torch.FloatTensor(inputs_scaled).to(device)
    outputs_tensor = torch.FloatTensor(outputs_scaled).to(device)  # 仅用于逆变换

    # 进行预测
    with torch.no_grad():
        pred_scaled = model(inputs_tensor)
        pred = output_scaler.inverse_transform(pred_scaled.cpu().numpy())
        true = output_scaler.inverse_transform(outputs_tensor.cpu().numpy())

    # 计算误差和欧氏距离
    errors = pred - true
    distances = np.sqrt(np.sum(errors ** 2, axis=1))

    # 输出表头
    print("\n" + "=" * 140)
    print("样本ID 预测X       真实X       误差X       预测Y       真实Y       误差Y       预测Z       真实Z       误差Z       欧氏距离")
    print("=" * 140)

    for i in range(len(pred)):
        print(f"{i:<4} "
              f"{pred[i,0]:<12.6f} {true[i,0]:<12.6f} {errors[i,0]:<12.6f} "
              f"{pred[i,1]:<12.6f} {true[i,1]:<12.6f} {errors[i,1]:<12.6f} "
              f"{pred[i,2]:<12.6f} {true[i,2]:<12.6f} {errors[i,2]:<12.6f} "
              f"{distances[i]:<12.6f}")

    print("=" * 140)

    # 统计摘要
    mean = np.mean(distances)
    median = np.median(distances)
    std = np.std(distances)
    minv = np.min(distances)
    maxv = np.max(distances)

    print("\n统计摘要:")
    print(f"平均欧氏距离: {mean:.6f} mm")
    print(f"中位数:       {median:.6f} mm")
    print(f"标准差:       {std:.6f} mm")
    print(f"最小值:       {minv:.6f} mm")
    print(f"最大值:       {maxv:.6f} mm")

    # 可选：保存结果到文件
    np.savez('evaluation_results.npz',
             pred=pred, true=true, errors=errors, distances=distances,
             mean=mean, median=median, std=std, min=minv, max=maxv)
    print("\n详细结果已保存为 evaluation_results.npz")


if __name__ == "__main__":
    main()