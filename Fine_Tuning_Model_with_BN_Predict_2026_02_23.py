# ---------- 模型定义（与训练时完全一致） ----------
class SimplifiedMLP_B(nn.Module):
    """方案B的简化MLP模型，使用LeakyReLU(0.618)激活函数"""

    def __init__(self, input_dim=4, output_dim=3):
        super(SimplifiedMLP_B, self).__init__()

        self.encoder = nn.Sequential(
            nn.Linear(input_dim, 32),
            nn.BatchNorm1d(32),
            nn.LeakyReLU(negative_slope=0.618),
            nn.Dropout(0.1),

            nn.Linear(32, 64),
            nn.BatchNorm1d(64),
            nn.LeakyReLU(negative_slope=0.618),
            nn.Dropout(0.1),
        )

        self.residual = nn.Linear(input_dim, 64)

        self.decoder = nn.Sequential(
            nn.Linear(64, 32),
            nn.LeakyReLU(negative_slope=0.618),
            nn.Linear(32, output_dim)
        )

        self._initialize_weights()

    def _initialize_weights(self):
        for m in self.modules():
            if isinstance(m, nn.Linear):
                nn.init.xavier_normal_(m.weight, gain=1.0)
                if m.bias is not None:
                    nn.init.constant_(m.bias, 0)
            elif isinstance(m, nn.BatchNorm1d):
                nn.init.constant_(m.weight, 1)
                nn.init.constant_(m.bias, 0)

    def forward(self, x):
        enc_out = self.encoder(x)
        res_out = self.residual(x)
        combined = enc_out + res_out
        return self.decoder(combined)

    def freeze_batch_norm(self):
        """冻结BatchNorm层（评估时BN层自动使用训练好的统计量，无需冻结，但保留方法）"""
        for m in self.modules():
            if isinstance(m, nn.BatchNorm1d):
                m.eval()
                m.track_running_stats = False


